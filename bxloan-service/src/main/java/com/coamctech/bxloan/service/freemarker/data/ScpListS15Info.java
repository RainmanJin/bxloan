package com.coamctech.bxloan.service.freemarker.data;

/**
 * 质押财产清单(债券)
 */
public class ScpListS15Info extends ScpListBaseInfo{
	/**
	 * 债券名称<br>
	 * 注：与super中pledgeName相同
	 */
	private String bondName;
	/**
	 * 债券类型名称
	 */
	private String bondTypeName;
	/**
	 * 币种
	 */
	private String bondCurrencyName;
	/**
	 * 面值
	 */
	private String bondValue;
	/**
	 * 年利率
	 */
	private String bondYearIr;
	/**
	 * 发行日期
	 */
	private String bondIssueDate;
	/**
	 * 发行人名称
	 */
	private String bondPersonName;
	/**
	 * 到期日
	 */
	private String bondMaturityDate;
	public String getBondName() {
		return bondName;
	}
	public void setBondName(String bondName) {
		this.bondName = bondName;
	}
	public String getBondTypeName() {
		return bondTypeName;
	}
	public void setBondTypeName(String bondTypeName) {
		this.bondTypeName = bondTypeName;
	}
	public String getBondValue() {
		return bondValue;
	}
	public void setBondValue(String bondValue) {
		this.bondValue = bondValue;
	}
	public String getBondYearIr() {
		return bondYearIr;
	}
	public void setBondYearIr(String bondYearIr) {
		this.bondYearIr = bondYearIr;
	}
	public String getBondIssueDate() {
		return bondIssueDate;
	}
	public void setBondIssueDate(String bondIssueDate) {
		this.bondIssueDate = bondIssueDate;
	}
	public String getBondPersonName() {
		return bondPersonName;
	}
	public void setBondPersonName(String bondPersonName) {
		this.bondPersonName = bondPersonName;
	}
	public String getBondMaturityDate() {
		return bondMaturityDate;
	}
	public void setBondMaturityDate(String bondMaturityDate) {
		this.bondMaturityDate = bondMaturityDate;
	}
	public String getBondCurrencyName() {
		return bondCurrencyName;
	}
	public void setBondCurrencyName(String bondCurrencyName) {
		this.bondCurrencyName = bondCurrencyName;
	}
}
