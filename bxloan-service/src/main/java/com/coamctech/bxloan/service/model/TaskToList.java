package com.coamctech.bxloan.service.model;

/**
 * 已办列表查询结果
 * @author xc
 */
public class TaskToList {
	
	private String comments;//任务提示消息
	private String stageNameCn;//任务当前环节,
	private String subjectNameCn;//任务名称,
	private String taskAssignDate;//任务接收时间,
	private String taskId;//任务ID,
	private String taskStartDate;//任务创建时间,
	private String taskStatus;//任务状态,
	private String workflowId;//流程ID,
	private String workflowType;//任务类型
	
	////////////////////////
	/////GETTERS&SETTERS/////
	////////////////////////
	public String getComments() {
		return comments;
	}
	public void setComments(String comments) {
		this.comments = comments;
	}
	public String getStageNameCn() {
		return stageNameCn;
	}
	public void setStageNameCn(String stageNameCn) {
		this.stageNameCn = stageNameCn;
	}
	public String getSubjectNameCn() {
		return subjectNameCn;
	}
	public void setSubjectNameCn(String subjectNameCn) {
		this.subjectNameCn = subjectNameCn;
	}
	public String getTaskAssignDate() {
		return taskAssignDate;
	}
	public void setTaskAssignDate(String taskAssignDate) {
		this.taskAssignDate = taskAssignDate;
	}
	public String getTaskId() {
		return taskId;
	}
	public void setTaskId(String taskId) {
		this.taskId = taskId;
	}
	public String getTaskStartDate() {
		return taskStartDate;
	}
	public void setTaskStartDate(String taskStartDate) {
		this.taskStartDate = taskStartDate;
	}
	public String getTaskStatus() {
		return taskStatus;
	}
	public void setTaskStatus(String taskStatus) {
		this.taskStatus = taskStatus;
	}
	public String getWorkflowId() {
		return workflowId;
	}
	public void setWorkflowId(String workflowId) {
		this.workflowId = workflowId;
	}
	public String getWorkflowType() {
		return workflowType;
	}
	public void setWorkflowType(String workflowType) {
		this.workflowType = workflowType;
	}
	
}
