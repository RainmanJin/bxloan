package com.coamctech.bxloan.service.model.applyinfo;

import java.util.ArrayList;
import java.util.List;

public class ApplyInfoLineGroupVO {

	private String title;
	private Integer lineGroupCd;
	private List<ApplyInfoLineVO> lines = new ArrayList<ApplyInfoLineVO>();

	//////////////////////////
	////GETTERS&SETTERS/////
	///////////////////////////	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public Integer getLineCount() {
		return this.lines==null||this.lines.isEmpty()?0:this.lines.size();
	}
	public List<ApplyInfoLineVO> getLines() {
		return lines;
	}
	public void setLines(List<ApplyInfoLineVO> lines) {
		this.lines = lines;
	}
	public Integer getLineGroupCd() {
		return lineGroupCd;
	}
	public void setLineGroupCd(Integer lineGroupCd) {
		this.lineGroupCd = lineGroupCd;
	}
	
}
