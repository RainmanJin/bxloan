package com.coamctech.bxloan.service.model.bizapply;

import java.math.BigDecimal;

public class OtherIncomeCommonVO {
	private Long id;
	private Long projectId;
	private String type;
	private String time;
	private String content;
	private BigDecimal amount;
	private String incomeCostType;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getProjectId() {
		return projectId;
	}

	public void setProjectId(Long projectId) {
		this.projectId = projectId;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public BigDecimal getAmount() {
		return amount;
	}

	public void setAmount(BigDecimal amount) {
		this.amount = amount;
	}

	public String getIncomeCostType() {
		return incomeCostType;
	}

	public void setIncomeCostType(String incomeCostType) {
		this.incomeCostType = incomeCostType;
	}
}
