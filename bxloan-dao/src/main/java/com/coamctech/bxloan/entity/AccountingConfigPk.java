package com.coamctech.bxloan.entity;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Embeddable;
@Embeddable
public class AccountingConfigPk implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	@Column(name="BUSSTYPE",length=4)
	private String bussType;
	@Column(name="EVENTTYPE")
	private Integer eventType;
	@Column(name="ITEMID")
	private Integer itemId;
	@Column(name="REFCODE")
	private Integer refCode;
	@Column(name="SUITNO")
	private Integer suitNo;
	public String getBussType() {
		return bussType;
	}
	public void setBussType(String bussType) {
		this.bussType = bussType;
	}
	public Integer getEventType() {
		return eventType;
	}
	public void setEventType(Integer eventType) {
		this.eventType = eventType;
	}
	public Integer getItemId() {
		return itemId;
	}
	public void setItemId(Integer itemId) {
		this.itemId = itemId;
	}
	public Integer getRefCode() {
		return refCode;
	}
	public void setRefCode(Integer refCode) {
		this.refCode = refCode;
	}
	public Integer getSuitNo() {
		return suitNo;
	}
	public void setSuitNo(Integer suitNo) {
		this.suitNo = suitNo;
	}
}
